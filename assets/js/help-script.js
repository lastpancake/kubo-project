function myFunction() {
    var x = document.getElementById("myDIV");
    var btn = document.querySelector("#btn0");

    if (x.style.display == "block") {
        x.style.display = "none";
        btn.innerText = "+";
    } else {
        x.style.display = "block";
        btn.innerText = String.fromCharCode(0x2212);
    }
}

